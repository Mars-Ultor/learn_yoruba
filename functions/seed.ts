// SEED FUNCTION
// ═══════════════════════════════════════════════════════════

async function seed() {
  console.log('🌱 Starting comprehensive Yoruba curriculum seed...\n');

  // Clear existing data
  console.log('🗑️  Clearing existing data...');
  const collections = ['lessons', 'vocabulary'];
  for (const col of collections) {
    const snapshot = await db.collection(col).get();
    const batch = db.batch();
    snapshot.docs.forEach((doc) => batch.delete(doc.ref));
    if (snapshot.docs.length > 0) await batch.commit();
  }

  // Seed lessons with phrases
  console.log(`\n📚 Seeding ${lessons.length} lessons...`);
  for (const lesson of lessons) {
    const { phrases, ...lessonData } = lesson;
    const lessonRef = db.collection('lessons').doc();
    await lessonRef.set({
      ...lessonData,
      createdAt: new Date().toISOString(),
      updatedAt: new Date().toISOString(),
    });

    // Add phrases as subcollection
    for (const phrase of phrases) {
      await lessonRef.collection('phrases').doc().set(phrase);
    }

    console.log(`  ✅ Lesson ${lesson.order}: ${lesson.title} (${phrases.length} phrases)`);
  }

  // Seed vocabulary
  console.log(`\n📖 Seeding ${vocabulary.length} vocabulary entries...`);
  const vocabBatchSize = 500; // Firestore batch limit
  for (let i = 0; i < vocabulary.length; i += vocabBatchSize) {
    const batch = db.batch();
    const chunk = vocabulary.slice(i, i + vocabBatchSize);
    for (const vocab of chunk) {
      const ref = db.collection('vocabulary').doc();
      batch.set(ref, {
        ...vocab,
        createdAt: new Date().toISOString(),
        updatedAt: new Date().toISOString(),
      });
    }
    await batch.commit();
  }
  console.log(`  ✅ ${vocabulary.length} vocabulary entries seeded`);

  // Summary
  const totalPhrases = lessons.reduce((sum, l) => sum + l.phrases.length, 0);
  console.log('\n═══════════════════════════════════════');
  console.log('🎉 Seed complete!');
  console.log(`   📚 ${lessons.length} lessons`);
  console.log(`   💬 ${totalPhrases} phrases`);
  console.log(`   📖 ${vocabulary.length} vocabulary entries`);
  console.log(`   📊 Categories: conversation, vocabulary, grammar, listening`);
  console.log(`   📊 Levels: beginner (A1-A2), intermediate (B1-B2), advanced (C1-C2)`);
  console.log('═══════════════════════════════════════\n');
}

seed()
  .then(() => process.exit(0))
  .catch((err) => {
    console.error('❌ Seed failed:', err);
    process.exit(1);
  });
